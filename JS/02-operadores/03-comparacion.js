let a = 10;

// operadores relacionales

console.log(a > 5); 
console.log(a >= 5); 
console.log(a < 5); 
console.log(a <= 5);

// operadores de igualdad

console.log(a == 10); // igual
console.log(a != 10); // distinto
console.log(a === '10'); // compara valor y tipo de dato
console.log(a !== 5); // distinto tipo de dato y de valor


