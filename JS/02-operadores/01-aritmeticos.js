let a = 5;
let b = 10;

console.log(a + b, "suma"); // suma
console.log(a - b, "resta"); // resta
console.log(a * b, "multiplicacion"); // multiplicacion
console.log(a / b, "division"); // division
console.log(a % b, "modulo"); // resto de division
console.log(a ** b, "potencia"); // potencia

// incrementar

console.log(++a); // incrementa el valor de a en 1 en imprime
console.log(a++); // imprime e incrementa el valor de a en 1
console.log(a); // valor real de a

// decrementar

console.log(--b); // incrementa el valor de a en 1 en imprime
console.log(b--); // imprime e incrementa el valor de a en 1
console.log(b); // valor real de a




